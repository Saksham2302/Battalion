    private void move(String str)
    {
        try {
            path_file paths=new path_file();
            Path temp = Files.copy(Paths.get(str),Paths.get(paths.images + "\\" +new_filename), StandardCopyOption.REPLACE_EXISTING);
            if(temp !=null)
                System.out.println("File renamed and moved successfully");
            else
                System.out.println("Failed to move the file");
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "An unexpected error occurred!");
        }
    }
    
