    private void insert_buttonAMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_insert_buttonAMouseClicked
        // TODO add your handling code here:
        String army_name = retrieve_Army_No();
        boolean flag = true;
        String Query = "";
        if (!army_name.equals("")) {
            String course = course_textfieldA.getText().trim();
            String grading = grading_textfieldA.getText().trim();
            String institute = institute_textfieldA.getText().trim();
            String from_date = validate_date((String) from_date_comboboxA.getSelectedItem(), (String) from_month_comboboxA.getSelectedItem(), (String) from_year_comboboxA.getSelectedItem());
            String to_date = validate_date((String) to_date_comboboxA.getSelectedItem(), (String) to_month_comboboxA.getSelectedItem(), (String) to_year_comboboxA.getSelectedItem());
            if (course.equals("")) {
                Query = Query + "Course field cannot be empty!\n";
                flag = false;
            }
            if (institute.equals("")) {
                Query = Query + "Institute field cannot be empty!\n";
                flag = false;
            }
            if (from_date.equals("")) {
                Query = Query + "From date is not valid!\n";
                flag = false;
            }
            if (to_date.equals("")) {
                Query = Query + "To date is not valid!\n";
                flag = false;
            }
            if (grading.equals("")) {
                Query = Query + "Grading field cannot be empty!\n";
                flag = false;
            }

            if (local_radio.isSelected()) {
                if (!flag) {
                    JOptionPane.showMessageDialog(null, Query, "Error", JOptionPane.ERROR_MESSAGE);
                } else {
                    try {
                        PreparedStatement pstmt = con.prepareStatement("Insert into Course_record_local values(?, ?, ?, ?, ?, ?, ?);");
                        pstmt.setString(1, army_no_panelA);
                        pstmt.setString(2, army_name);
                        pstmt.setString(3, course);
                        pstmt.setString(4, institute);
                        pstmt.setString(5, from_date);
                        pstmt.setString(6, to_date);
                        pstmt.setString(7, grading);
                        pstmt.executeUpdate();
                        JOptionPane.showMessageDialog(null, "Entry Successful", "Successful", JOptionPane.WARNING_MESSAGE);

                        course_textfieldA.setText("");
                        institute_textfieldA.setText("");
                        grading_textfieldA.setText("");
                        service_number_textfield.setText("");
                        name.setText(">XXXXXXX");
                        company.setText(">XXXXXXX");
                        rank.setText(">XXXXXXX");
                        unit.setText(">XXXXXXX");
                        trade.setText(">XXXXXXX");
                        from_date_comboboxA.setSelectedItem("DD");
                        from_month_comboboxA.setSelectedItem("MM");
                        from_year_comboboxA.setSelectedItem("YYYY");
                        to_date_comboboxA.setSelectedItem("DD");
                        to_month_comboboxA.setSelectedItem("MM");
                        to_year_comboboxA.setSelectedItem("YYYY");
                        buttonGroup1.clearSelection();

                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, e);

                    }
                }

            } else if (army_radio.isSelected()) {
                if (!flag) {
                    JOptionPane.showMessageDialog(null, Query, "Error", JOptionPane.ERROR_MESSAGE);
                } else {
                    try {
                        PreparedStatement pstmt = con.prepareStatement("Insert into Course_record_army values(?, ?, ?, ?, ?, ?, ?);");
                        pstmt.setString(1, army_no_panelA);
                        pstmt.setString(2, army_name);
                        pstmt.setString(3, course);
                        pstmt.setString(4, institute);
                        pstmt.setString(5, from_date);
                        pstmt.setString(6, to_date);
                        pstmt.setString(7, grading);
                        pstmt.executeUpdate();
                        JOptionPane.showMessageDialog(null, "Entry Successful", "Successful", JOptionPane.WARNING_MESSAGE);

                        course_textfieldA.setText("");
                        institute_textfieldA.setText("");
                        grading_textfieldA.setText("");
                        service_number_textfield.setText("");
                        name.setText(">XXXXXXX");
                        company.setText(">XXXXXXX");
                        rank.setText(">XXXXXXX");
                        unit.setText(">XXXXXXX");
                        trade.setText(">XXXXXXX");
                        from_date_comboboxA.setSelectedItem("DD");
                        from_month_comboboxA.setSelectedItem("MM");
                        from_year_comboboxA.setSelectedItem("YYYY");
                        to_date_comboboxA.setSelectedItem("DD");
                        to_month_comboboxA.setSelectedItem("MM");
                        to_year_comboboxA.setSelectedItem("YYYY");
                        buttonGroup1.clearSelection();

                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, e);

                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Select course type (Army/Local)!", "Error", JOptionPane.ERROR_MESSAGE);

            }
        }
    }//GEN-LAST:event_insert_buttonAMouseClicked

