    private void current_time_date(String time_date){
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("dd-MM-yyyy HH:mm:ss");  
        LocalDateTime now = LocalDateTime.now();  
        String[] day=dtf.format(now).split(" ");  
        if(time_date.equals("date_from")){
            String date[]=day[0].split("-");
            letter_date_comboboxA.setSelectedItem(date[0]);
            letter_month_comboboxA.setSelectedItem(date[1]);
            letter_year_comboboxA.setSelectedItem(date[2]);
        }
        else if(time_date.equals("date_to")){
            String date[]=day[0].split("-");
            despatch_date_comboboxA.setSelectedItem(date[0]);
            despatch_month_comboboxA.setSelectedItem(date[1]);
            despatch_year_comboboxA.setSelectedItem(date[2]);
        }
             
    }
    
