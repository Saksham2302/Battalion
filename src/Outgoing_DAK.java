    private void print_buttonBMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_print_buttonBMouseClicked
        title_label.grabFocus();
        try{

        java.sql.Statement statement = c.createStatement();
            ResultSet resultSet = statement.executeQuery("select * from out_dak");
            XSSFWorkbook workbook = new XSSFWorkbook();
            XSSFSheet spreadsheet =null;
            spreadsheet = workbook.createSheet("out_dak");
            
            XSSFRow row = spreadsheet.createRow(0);
            XSSFCell cell;
            cell = row.createCell(0);
            cell.setCellValue("Letter Number");
            cell = row.createCell(1);
            cell.setCellValue("Date of Letter");
            cell = row.createCell(2);
            cell.setCellValue("Date of Despatch");
            cell = row.createCell(3);
            cell.setCellValue("To Whom");
            cell = row.createCell(4);
            cell.setCellValue("Subject");
            cell = row.createCell(5);
            cell.setCellValue("Classification");
            cell = row.createCell(6);
            cell.setCellValue("Mode Of Despatch");
            int i = 1;
            
            while(resultSet.next()) {
                row = spreadsheet.createRow(i);
                cell = row.createCell(0);
                cell.setCellValue(resultSet.getString("letter_no"));
                cell = row.createCell(1);
                cell.setCellValue(resultSet.getString("dol"));
                cell = row.createCell(2);
                cell.setCellValue(resultSet.getString("dod"));
                cell = row.createCell(3);
                cell.setCellValue(resultSet.getString("to_"));
                cell = row.createCell(4);
                cell.setCellValue(resultSet.getString("subject"));
                cell = row.createCell(5);
                cell.setCellValue(resultSet.getString("classification"));
                cell = row.createCell(6);
                cell.setCellValue(resultSet.getString("mode"));
                i++;
            }
            path_file paths=new path_file();
            FileOutputStream out = new FileOutputStream(new File(paths.csv + "\\" +"out_dak.xlsx"));
            workbook.write(out);
            JOptionPane.showMessageDialog(null,"out_dak.xlsx written successfully");
            Desktop.getDesktop().open(new File(paths.csv + "/out_dak.xlsx"));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"out_dak.xlsx not created");
        }
        
    }//GEN-LAST:event_print_buttonBMouseClicked

