    private void insert_buttonAMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_insert_buttonAMouseClicked
        // TODO add your handling code here:
        String year = (String)letter_year_comboboxA.getSelectedItem();
        String month = (String)letter_month_comboboxA.getSelectedItem();
        String date = (String)letter_date_comboboxA.getSelectedItem();
        String date_let = date+"-"+month+"-"+year;
             
               
        String year1 = (String)despatch_year_comboboxA.getSelectedItem();
        String month1 = (String)despatch_month_comboboxA.getSelectedItem();
        String date1 = (String)despatch_date_comboboxA.getSelectedItem();
        String date_des = date1+"-"+month1+"-"+year1;
        
        boolean all_check = (letter_number_textfieldA.getText()!=null && !letter_number_textfieldA.getText().trim().isEmpty()) && (to_textfieldA.getText()!=null && !to_textfieldA.getText().trim().isEmpty()) && (subject_textfieldA.getText()!=null && !subject_textfieldA.getText().trim().isEmpty()) && (mode_textfieldA.getText()!=null && !mode_textfieldA.getText().trim().isEmpty());
        
        boolean cal = false;
        boolean cal1 = false;
        boolean da = year!= "YYYY" && year1!= "YYYY" && month!= "MM" && month1!= "MM" && date!= "DD" && date1!= "DD";
        if(da){
        int y = Integer.parseInt(year);
        int m = Integer.parseInt(month);
        int d = Integer.parseInt(date);
        int y1 = Integer.parseInt(year1);
        int m1 = Integer.parseInt(month1);
        int d1 = Integer.parseInt(date1);
        cal = !((y%4==0 && m==2 &&  (d>29)  )   || (  (m==2 || m==4 || m==6 || m==9 || m==11)  && d==31  )    ||  (m==2 && d>28 && y%4!=0));
        cal1 = !((y1%4==0 && m1==2 &&  (d1>29)  )   || (  (m1==2 || m1==4 || m1==6 || m1==9 || m1==11)  && d1==31  )    ||  (m1==2 && d1>28 && y1%4!=0));
        }
        if(all_check && cal && cal1){
            try {
                String q = "insert into out_dak values(?,?,?,?,?,?,?)";
                java.sql.PreparedStatement st = c.prepareStatement(q);
                st.setString(1,letter_number_textfieldA.getText().trim());
                st.setString(2,date_let);
                st.setString(3,date_des);
                st.setString(4,to_textfieldA.getText().trim());
                st.setString(5,subject_textfieldA.getText().trim());
                st.setString(6, (String) classification_comboboxA.getSelectedItem());
                st.setString(7,mode_textfieldA.getText().trim());
                //ResultSet rs=st.executeQuery();
                st.execute();
                JOptionPane.showMessageDialog(null, "Data Inserted Successfully");
                letter_number_textfieldA.grabFocus();
                letter_number_textfieldA.setText("");
                to_textfieldA.setText("");
                subject_textfieldA.setText("");
                mode_textfieldA.setText("");
                letter_date_comboboxA.setSelectedItem("DD");
                letter_month_comboboxA.setSelectedItem("MM");
                letter_year_comboboxA.setSelectedItem("YYYY");
                despatch_date_comboboxA.setSelectedItem("DD");
                despatch_month_comboboxA.setSelectedItem("MM");
                despatch_year_comboboxA.setSelectedItem("YYYY");
                classification_comboboxA.setSelectedItem("UNCLAS");
                
            } catch (SQLException ex) {
                java.util.logging.Logger.getLogger(State_VEH.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            }
        }
        else if(cal == false || cal1 == false){
            
            JOptionPane.showMessageDialog(null, "Invalid Date");
        }
        else if(all_check == false){
            if((letter_number_textfieldA.getText()==null || letter_number_textfieldA.getText().trim().isEmpty())){
                JOptionPane.showMessageDialog(null, "Letter Number Field is Empty.");
                letter_number_textfieldA.grabFocus();
            }
            else if((mode_textfieldA.getText()==null || mode_textfieldA.getText().trim().isEmpty())){
                JOptionPane.showMessageDialog(null, "Mode of Despatch Field is Empty.");
                mode_textfieldA.grabFocus();
            }
            else if((to_textfieldA.getText()==null || to_textfieldA.getText().trim().isEmpty())){
                JOptionPane.showMessageDialog(null, "To Field is Empty.");
                to_textfieldA.grabFocus();
            }
            else if((subject_textfieldA.getText()==null || subject_textfieldA.getText().trim().isEmpty())){
                JOptionPane.showMessageDialog(null, "Subject Field is Empty.");
                subject_textfieldA.grabFocus();
            }
        }
    }//GEN-LAST:event_insert_buttonAMouseClicked

