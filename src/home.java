    private void continue_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_continue_buttonActionPerformed
        bg.setVisible(true);
        
        H0_notifications.setBackground(new java.awt.Color(240,240,240));
        H1_modules.setBackground(new java.awt.Color(240,240,240));
        H2_coys.setBackground(new java.awt.Color(240,240,240));
        H5_ourheroes.setBackground(new java.awt.Color(240,240,240));
        H6_tenures.setBackground(new java.awt.Color(240,240,240));
        H7_exco.setBackground(new java.awt.Color(240,240,240));
        H8_history.setBackground(new java.awt.Color(240,240,240));
        H9_achievements.setBackground(new java.awt.Color(240,240,240));
        H10_contact.setBackground(new java.awt.Color(240,240,240));

        intro_panel.setVisible(false);
        
        String count="0";
        
        try {
            Statement stmt = db.con.createStatement();
            rs = stmt.executeQuery("SELECT count(*) FROM notify where status=0;");
            while (rs.next()) {
                count = rs.getString("count(*)");
            }
            
            if(count.equals("1"))
                JOptionPane.showMessageDialog(null, "You have a notification");
            else if(count.equals("0")==false)
                JOptionPane.showMessageDialog(null, "You have "+count+" notifications");
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
        
    }//GEN-LAST:event_continue_buttonActionPerformed

